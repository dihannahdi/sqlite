٢- طريق ابن الضريس في كتابه «فضائل القرآن...» : قال ابن الضريس: "أخبرنا أحمد قال: حدثنا محمد قال: أخبرنا محمد بن عبد الله بن أبي جعفر الرازي قال: قال عمر بن هارون قال: حدثنا عمر بن عطاء عن أبيه عن ابن عباس -رضي الله عنهما- قال: "أول ما نزل من القرآن بمكة، وما أنزل منه بالمدينة الأول، فكانت إذا نزلت فاتحة سورة بمكة فكتبت بمكة، ثم يزيد الله فيها ما يشاء، وكان أول ما أنزل من القرآن: اقرأ باسم ربك الذي خلق... " (٤) ثم سرد السور المكية وهي ست وثمانون سورة، والسور المدنية وهي ثمان وعشرون سورة، وهي: البقرة ثم الأنفال، ثم آل عمران ثم الأحزاب، ثم الممتحنة، ثم النساء، ثم الزلزلة، ثم الحديد، ثم سورة محمد، ثم الرعد، ثم الرحمن، ثم الإنسان، ثم الطلاق، ثم البينة، ثم الحشر، ثم النصر، ثم الحج، ثم المنافقون، ثم المجادلة، ثم الحجرات، ثم التحريم، ثم الجمعة، ثم التغابن، ثم الصف، ثم الفتح، ثم المائدة ثم التوبة.

وإسناده ضعيف من أجل عمر بن هارون، ولكن رواية الزركشي والتي بسند آخر تعد شاهدًا لهذا الطريق (٥) .

٣- طريق النحاس في كتابه «الناسخ والمنسوخ...»

قال أبو جعفر النحاس: "حدثني يموت بن المزروع، قال: حدثنا أبو حاتم سهل بن محمد السجستاني، قال: حدثنا أبو عبيدة معمر بن المثنى التيمي، قال: حدثنا يونس بن حبيب، قال: سمعت أبا عمرو بن العلاء يقول: سألت مجاهدًا عن تلخيص آي المدني من المكي، فقال: سألت ابن عباس عن ذلك فقال: سورة الأنعام نزلت بمكة جملة واحدة، فهي مكية إلا ثلاث آيات منها نزلت بالمدينة، فهن مدنيات قُلْ تَعَالَوْا أَتْلُ مَا حَرَّمَ رَبُّكُمْ إلى تمام الآيات الثلاث... وما تقدم من السور فهن مدنيات، أعني سورة البقرة وآل عمران والنساء والمائدة...." (٦) .

وساقها السيوطي منسوبة إلى كتابه «الناسخ والمنسوخ» سياقًا أكمل منها، وفيها تعداد للسور المكية والمدنية، ثم قال: "هكذا أخرجه بطوله وإسناده جيد، رجاله كلهم ثقات من علماء العربية المشهورين" (٧) .

والحقيقة أن الإسناد فيه ضعف من أجل السجستاني وأبي عبيدة. (٨)
٤- طريق ابن عبد الكافي في كتابه: "بيان عدد سور القرآن وآياته.."

قال ابن عبد الكافي: "سمعت الإمام أبا الحسن الفارسي رحمه الله قال: سمعت الإمام أبا بكر أحمد بن الحسين أنه قال: روي عن عبد الله بن عمير عن أبيه عن عثمان بن عطاء الخراساني، عن أبيه عن ابن عباس..." (٩) فذكر السور المكية والمدنية معًا.

٥- طريق البيهقي في كتابه: «دلائل النبوة ومعرفة أحوال صاحب الشريعة» .

روى البيهقي الرواية بإسنادين وصحح أحدهما، وهو الإسناد:

"قال البيهقي: أخبرنا أبو عبد الله الحافظ، قال: أخبرنا أبو محمد بن زياد العدل، قال: حدثنا محمد بن إسحاق، قال: حدثنا يعقوب بن إبراهيم الدورقي قال: حدثنا أحمد بن نصر بن مالك الخزاعي، قال: علي بن الحسين بن الواقد، عن أبيه قال: حدثنا يزيد النحوي عن عكرمة والحسن بن أبي الحسن" (١٠) ثم ذكر السور المكية...، والسور المدنية.

والسور المدنية هي: المطففين، والبقرة، وآل عمران، والأنفال، والأحزاب، والمائدة، والممتحنة، والنساء، والزلزلة، والحديد، ومحمد، والرعد، والرحمن، والإنسان، والطلاق، والبينة، والحشر، والنصر، والنور، والحج، والمنافقون، والمجادلة، والحجرات، والتحريم، والصف، والجمعة، والتغابن، والفتح، والتوبة.
وهي تسع وعشرون سورة، وإسناد الرواية صحيح (١١) .

ب) الرواية الثانية عن قتادة، وقد جاءت من ثلاث طرق كلها صحيحة إلى قتادة. (١٢)
١- طريق حارث المحاسبي في كتابه: «فهم القرآن» .
قال الحارث -رحمه الله-: "حدثنا شريح، قال: حدثنا سفيان عن معمر عن قتادة قال: "السور المدنية: البقرة، وآل عمران، والنساء، والمائدة، والأنفال، والتوبة، والرعد، والحجر، والنحل، والنور، والأحزاب، وسورة محمد (صلى الله عليه وسلم) والفتح، والحجرات، والحديد، والمجادلة، والممتحنة، والصف، والجمعة، والمنافقون، والتغابن، والنساء الصغرى، و "يا أيها النبي لم تحرم"، و "لم يكن"، و "إذا جاء نصر الله والفتح"، و "قل هو الله أحد"، وهو يشك في "أرأيت" (١٣) .

وهي سبع وعشرون سورة، وما عداها كلها مكية.

٢- طريق ابن الأنباري في كتابه: «الرد على من خالف مصحف عثمان» قال ابن الأنباري -رحمه الله-: "حدثنا إسماعيل بن إسحاق القاضي، حدثنا حجاج بن منهال، حدثنا همام عن قتادة قال: نزل بالمدينة من القرآن البقرة، وآل عمران، والنساء، والمائدة، والأنفال، وبراءة، والرعد، والنحل، والحج، والنور، والأحزاب، ومحمد، والفتح، والحجرات، والرحمن، والحديد، والمجادلة، والحشر، والممتحنة، والصف، والجمعة، والمنافقون، والتغابن، والطلاق، ويأيها النبي لم تحرم إلى رأس العشر، وإذا زلزلت، وإذا جاء نصر الله هؤلاء السور نزلت بالمدينة، وسائر القرآن نزل بمكة" (١٤) .

فهذه سبع وعشرون سورة مدنية، ويؤيد هذه الرواية المذكورة من الطريقين ما رواه ابن سعد في الطبقات:
قال: "أنبأنا الواقدي حدثني قدامة بن موسى، عن أبي سلمة الحضرمي سمعت ابن عباس، قال: سألت أبي بن كعب عما نزل من القرآن بالمدينة، فقال نزل بها سبع وعشرون سورة، وسائرها بمكة" (١٥) .

٣- طريق أبي عمرو الداني في كتابه: «البيان في عد آي القرآن» .

قال الحافظ: "أخبرنا فارس بن أحمد، قال: أنا أحمد بن محمد، قال: أنا أحمد بن عثمان، قال: أنا الفضل بن شاذان، قال: أنا إبراهيم بن موسى، قال: أنا يزيد بن زريع قال: أنا سعيد، عن قتادة، قال: المدني البقرة، وآل عمران، والنساء، والمائدة، والأنفال، وبراءة، والرعد، والحج، والنور، والأحزاب، و "الذين كفروا"، و "إنا فتحنا لك فتحا مبينا"، و "يا أيها الذين آمنوا لا تقدموا بين يدي الله ورسوله"، والمسبحات من سورة الحديد إلى "يا أيها النبي إذا طلقتم النساء" (١٦) ، و "يا أيها النبيّ لم تحرم"، و "لم يكن الذين كفروا"، و "إذا زلزلت"، و "إذا جاء نصر الله" مدني، وما بقي مكي (١٧) فهي خمس وعشرون سورة، ثم ذكر السور التي بعضها مكي وبعضها مدني.

ج) الرواية الثالثة عن جابر بن زيد التابعي رواها عنه أبو عمرو الداني بإسناده: "قال: أخبرنا فارس بن أحمد، قال: أنا أحمد بن محمد، قال: أنا أحمد بن عثمان، قال: أنا الفضل، قال: أنا أحمد بن يزيد، قال: أنا أبو كامل فضيل بن حسين، قال: أنا حسان بن إبراهيم، قال: أنا أمية الأزدي، عن جابر بن زيد قال:.... "ثم سرد السور المكية على ترتيب نزولها وهي خمس وثمانون سورة، ثم قال:

"وأنزل عليه بعد ما قدم المدينة سورة البقرة، ثم آل عمران، ثم الأنفال، ثم الأحزاب، ثم المائدة، ثم الممتحنة، ثم النساء، ثم "إذا زلزلت"، ثم الحديد، ثم سورة محمد (صلى الله عليه وسلم) ، ثم الرعد، ثم الرحمن، ثم "هل أتى على الإنسان"، ثم سورة النساء القصرى، ثم "لم يكن الذين كفروا"، ثم الحشر، ثم "إذا جاء نصر الله والفتح"، ثم النور، ثم الحج، ثم المنافقون، ثم المجادلة، ثم الحجرات، ثم "يا أيها النبي لم تحرم"، ثم الجمعة، ثم التغابن، ثم سبح الحواريون، ثم "إنا فتحنا لك فتحًا"، ثم التوبة، ثم خاتمة الفرقان، فذلك ثمان وعشرون سورة" (١٨) .

د) الرواية الرابعة عن الإمام الزهري في كتابه: «تنزيل القرآن بمكة والمدينة» إلا أنها ضعيفة جدًا (١٩) .

ويضم إلى هذه الروايات بعض أقوال أهل العلم:

١- ذكر أبو داود سليمان بن نجاح في «مختصر التبيين لهجاء التنزيل» (٢٠) أن السور المدنية إحدى وعشرون سورة، وهن: البقرة، وآل عمران، والنساء، والمائدة، والأنفال، والتوبة، والنور، والأحزاب، والقتال، والفتح، والحجرات، والحديد، والمجادلة، والحشر، والممتحنة، والجمعة، والمنافقون، والطلاق، والتحريم، ولم يكن، والنصر.

