التعصيب معنى التعصيب: التعصيب في اللغة: مصدر عصب يعصب تعصيبا، واشتقاقه من العصب، وهو الشد والتقوية والإحاطة، ومنه قيل للعمائم: عصائب لإحاطتها بالرأس وشدها له.
وفي الاصطلاح: الإرث بلا تقدير.
معنى العصبة: العصبة في اللغة جمع عاصب، وهم القرابة الذكور، الذين يدلون بالذكور.
وفي الاصطلاح: هم الذين يرثون بلا تقدير.
وقيل: كل وارث إذا انفرد أخذ جميع المال، ويأخذ ما أبقيت الفروض، ويسقط إذا استغرقت الفروض المسألة.
قال في الرحبية:
فكل من أحرز كل المال ... من القرابات أو الموالي
